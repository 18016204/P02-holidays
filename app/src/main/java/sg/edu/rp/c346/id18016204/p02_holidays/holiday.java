package sg.edu.rp.c346.id18016204.p02_holidays;

public class holiday {

    private String newYear;
    private String date;
    public holiday (String newYear, String date){
        this.newYear = newYear;
        this.date = date;
    }
    public String getNewYear(){
        return newYear;
    }
    public String date(){
        return date;
    }
}
